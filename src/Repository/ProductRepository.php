    /**
     * @param string $query
     * @return mixed
     */
    public function findProductsByName(string $query)
    {
        $qb = $this->createQueryBuilder('p');
        $qb->where(

            $qb->expr()->orX(
                $qb->expr()->like('p.title', ':query'),
                $qb->expr()->like('p.nameCharacter', ':query')
            )
        )
            ->setParameter('query', '%' . $query . '%')
            ->setMaxResults(4);
        return $qb
            ->getQuery()
            ->getResult();
    }



